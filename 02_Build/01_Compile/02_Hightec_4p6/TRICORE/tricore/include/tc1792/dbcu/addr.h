#define DBCU_LEDATH_ADDR      0xF87FFA2C     /* "DBCU LMB Error Data High Register" */
#define DBCU_SRC_ADDR         0xF87FFAFC     /* "DBCU Service Request Control Register" */


#endif /* _HAVE_TRICORE_DBCU_ADDRESSES_H_ (block "DBCU") */


