/* block "P5" of TriCore TC1791 (10 SFRs) */

#ifndef _HAVE_TRICORE_P5_ADDRESSES_H_
#define _HAVE_TRICORE_P5_ADDRESSES_H_

#define P5_OUT_ADDR           0xF0001100     /* "Port 5 Output Register" */
#define P5_OMR_ADDR           0xF0001104     /* "Port 5 Output Modification Register" */
#define P5_IOCR0_ADDR         0xF0001110     /* "Port 5 Input/Output Control Register 0" */
#define P5_IOCR4_ADDR         0xF0001114     /* "Port 5 Input/Output Control Register 4" */
#define P5_IOCR8_ADDR         0xF0001118     /* "Port 5 Input/Output Control Register 8" */
#define P5_IOCR12_ADDR        0xF000111C     /* "Port 5 Input/Output Control Register 12" */
#define P5_IN_ADDR            0xF0001124     /* "Port 5 Input Register" */
#define P5_PDR0_ADDR          0xF0001140     /* "Port 5 Pad Driver Mode 0 Register" */
#define P5_PDR1_ADDR          0xF0001144     /* "Port 5 Pad Driver Mode 1 Register" */
#define P5_ESR_ADDR           0xF0001150     /* "Port 5 Emergency Stop Register" */


#endif /* _HAVE_TRICORE_P5_ADDRESSES_H_ (block "P5") */


