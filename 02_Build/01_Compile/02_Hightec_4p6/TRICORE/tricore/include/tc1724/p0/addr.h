/* block "P0" of TriCore TC1724 (10 SFRs) */

#ifndef _HAVE_TRICORE_P0_ADDRESSES_H_
#define _HAVE_TRICORE_P0_ADDRESSES_H_

#define P0_OUT_ADDR           0xF0000C00     /* "Port 0 Output Register" */
#define P0_OMR_ADDR           0xF0000C04     /* "Port 0 Output Modification Register" */
#define P0_IOCR0_ADDR         0xF0000C10     /* "Port 0 Input/Output Control Register 0" */
#define P0_IOCR4_ADDR         0xF0000C14     /* "Port 0 Input/Output Control Register 4" */
#define P0_IOCR8_ADDR         0xF0000C18     /* "Port 0 Input/Output Control Register 8" */
#define P0_IOCR12_ADDR        0xF0000C1C     /* "Port 0 Input/Output Control Register 12" */
#define P0_IN_ADDR            0xF0000C24     /* "Port 0 Input Register" */
#define P0_PDR0_ADDR          0xF0000C40     /* "Port 0 Pad Driver Mode 0 Register" */
#define P0_PDR1_ADDR          0xF0000C44     /* "Port 0 Pad Driver Mode 1 Register" */
#define P0_ESR_ADDR           0xF0000C50     /* "Port 0 Emergency Stop Register" */


#endif /* _HAVE_TRICORE_P0_ADDRESSES_H_ (block "P0") */


