/* block "SSC3" of TriCore TC1793 (15 SFRs) */

#ifndef _HAVE_TRICORE_SSC3_ADDRESSES_H_
#define _HAVE_TRICORE_SSC3_ADDRESSES_H_

#define SSC3_CLC_ADDR         0xF0310300     /* "SSC3 Clock Control Register" */
#define SSC3_PISEL_ADDR       0xF0310304     /* "Port Input Select Register" */
#define SSC3_ID_ADDR          0xF0310308     /* "Module Identification Register" */
#define SSC3_FDR_ADDR         0xF031030C     /* "SSC3 Fractional Divider Register" */
#define SSC3_CON_ADDR         0xF0310310     /* "Control Register" */
#define SSC3_BR_ADDR          0xF0310314     /* "Baud Rate Timer Reload Register" */
#define SSC3_SSOC_ADDR        0xF0310318     /* "Slave Select Output Control Register" */
#define SSC3_SSOTC_ADDR       0xF031031C     /* "Slave Select Output Timing Control Register" */
#define SSC3_TB_ADDR          0xF0310320     /* "Transmit Buffer Register" */
#define SSC3_RB_ADDR          0xF0310324     /* "Receive Buffer Register" */
#define SSC3_STAT_ADDR        0xF0310328     /* "Status Register" */
#define SSC3_EFM_ADDR         0xF031032C     /* "Error Flag Modification Register" */
#define SSC3_TSRC_ADDR        0xF03103F4     /* "Transmit Interrupt Service Request Control Register" */
#define SSC3_RSRC_ADDR        0xF03103F8     /* "Receive Interrupt Service Request Control Register" */
#define SSC3_ESRC_ADDR        0xF03103FC     /* "Error Interrupt Service Request Control Register" */


#endif /* _HAVE_TRICORE_SSC3_ADDRESSES_H_ (block "SSC3") */


