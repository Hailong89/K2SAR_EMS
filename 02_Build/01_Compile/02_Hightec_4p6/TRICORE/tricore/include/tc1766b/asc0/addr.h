/* block "ASC0 LL" of TriCore TC1766B (13 SFRs) */

#ifndef _HAVE_TRICORE_ASC0_ADDRESSES_H_
#define _HAVE_TRICORE_ASC0_ADDRESSES_H_

#define ASC0_CLC_ADDR         0xF0000A00     /* "ASC Clock Control Register" */
#define ASC0_PISEL_ADDR       0xF0000A04     /* "ASC Peripheral Input Select Register" */
#define ASC0_ID_ADDR          0xF0000A08     /* "ASC Module Identification Register" */
#define ASC0_CON_ADDR         0xF0000A10     /* "ASC Control Register" */
#define ASC0_BG_ADDR          0xF0000A14     /* "ASC Baudrate Timer Reload Register" */
#define ASC0_FDV_ADDR         0xF0000A18     /* "ASC Fractional Divider Register" */
#define ASC0_TBUF_ADDR        0xF0000A20     /* "ASC Transmit Buffer Register" */
#define ASC0_RBUF_ADDR        0xF0000A24     /* "ASC Receive Buffer Register" */
#define ASC0_WHBCON_ADDR      0xF0000A50     /* "ASC Hardware Bits Control Register" */
#define ASC0_TSRC_ADDR        0xF0000AF0     /* "ASC Transmit Interrupt Service Request Control Register" */
#define ASC0_RSRC_ADDR        0xF0000AF4     /* "ASC Receive Interrupt Service Request Control Register" */
#define ASC0_ESRC_ADDR        0xF0000AF8     /* "ASC Error Interrupt Service Request Control Register" */
#define ASC0_TBSRC_ADDR       0xF0000AFC     /* "ASC Transmit Buffer Service Request Control Register" */


#endif /* _HAVE_TRICORE_ASC0_ADDRESSES_H_ (block "ASC0 LL") */


