#ifndef _HAVE_TRICORE_SSC0_ADDRESSES_H_
#define _HAVE_TRICORE_SSC0_ADDRESSES_H_

#define SSC0_CLC_ADDR         0xF0100100     /* "SSC Clock Control Register" */
#define SSC0_PISEL_ADDR       0xF0100104     /* "SSC Port Input Select Register" */
#define SSC0_ID_ADDR          0xF0100108     /* "SSC Module Identification Register" */
#define SSC0_FDR_ADDR         0xF010010C     /* "SSC Fractional Divider Register" */
#define SSC0_CON_ADDR         0xF0100110     /* "SSC Control Register" */
#define SSC0_BR_ADDR          0xF0100114     /* "SSC Baudrate Timer Reload Register" */
#define SSC0_SSOC_ADDR        0xF0100118     /* "Slave Select Output Control Register" */
#define SSC0_SSOTC_ADDR       0xF010011C     /* "Slave Select Output Timing Control Register" */
#define SSC0_TB_ADDR          0xF0100120     /* "SSC Transmit Buffer Register" */
#define SSC0_RB_ADDR          0xF0100124     /* "SSC Receive Buffer Register" */
#define SSC0_STAT_ADDR        0xF0100128     /* "SSC Status Register" */
#define SSC0_EFM_ADDR         0xF010012C     /* "SSC Error Flag Modification Register" */
#define SSC0_TSRC_ADDR        0xF01001F4     /* "SSC Transmit Interrupt Service Request Control Register" */
#define SSC0_RSRC_ADDR        0xF01001F8     /* "SSC Receive Interrupt Service Request Control Register" */
#define SSC0_ESRC_ADDR        0xF01001FC     /* "SSC Error Interrupt Service Request Control Register" */


#endif /* _HAVE_TRICORE_SSC0_ADDRESSES_H_ (block "SSC0 LL0") */


