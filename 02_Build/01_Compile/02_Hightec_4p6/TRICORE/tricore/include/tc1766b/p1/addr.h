/* block "P1 LL" of TriCore TC1766B (9 SFRs) */

#ifndef _HAVE_TRICORE_P1_ADDRESSES_H_
#define _HAVE_TRICORE_P1_ADDRESSES_H_

#define P1_OUT_ADDR           0xF0000D00     /* "Port 1 Data Output Register" */
#define P1_OMR_ADDR           0xF0000D04     /* "Port 1 Output Modification Register" */
#define P1_IOCR0_ADDR         0xF0000D10     /* "Input/Output Control Register 0" */
#define P1_IOCR4_ADDR         0xF0000D14     /* "Input/Output Control Register 4" */
#define P1_IOCR8_ADDR         0xF0000D18     /* "Input/Output Control Register 8" */
#define P1_IOCR12_ADDR        0xF0000D1C     /* "Input/Output Control Register 12" */
#define P1_IN_ADDR            0xF0000D24     /* "Port 1 Input Register" */
#define P1_PDR_ADDR           0xF0000D40     /* "Port 1 Pad Driver Mode Register" */
#define P1_ESR_ADDR           0xF0000D50     /* "Port 1 Emergency Stop Register" */


#endif /* _HAVE_TRICORE_P1_ADDRESSES_H_ (block "P1 LL") */


