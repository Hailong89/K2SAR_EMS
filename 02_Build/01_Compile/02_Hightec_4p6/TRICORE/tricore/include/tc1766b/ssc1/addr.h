/* block "SSC0 LL1" of TriCore TC1766B (15 SFRs) */

#ifndef _HAVE_TRICORE_SSC1_ADDRESSES_H_
#define _HAVE_TRICORE_SSC1_ADDRESSES_H_

#define SSC1_CLC_ADDR         0xF0100200     /* "SSC Clock Control Register" */
#define SSC1_PISEL_ADDR       0xF0100204     /* "SSC Port Input Select Register" */
#define SSC1_ID_ADDR          0xF0100208     /* "SSC Module Identification Register" */
#define SSC1_FDR_ADDR         0xF010020C     /* "SSC Fractional Divider Register" */
#define SSC1_CON_ADDR         0xF0100210     /* "SSC Control Register" */
#define SSC1_BR_ADDR          0xF0100214     /* "SSC Baudrate Timer Reload Register" */
#define SSC1_SSOC_ADDR        0xF0100218     /* "Slave Select Output Control Register" */
#define SSC1_SSOTC_ADDR       0xF010021C     /* "Slave Select Output Timing Control Register" */
#define SSC1_TB_ADDR          0xF0100220     /* "SSC Transmit Buffer Register" */
#define SSC1_RB_ADDR          0xF0100224     /* "SSC Receive Buffer Register" */
#define SSC1_STAT_ADDR        0xF0100228     /* "SSC Status Register" */
#define SSC1_EFM_ADDR         0xF010022C     /* "SSC Error Flag Modification Register" */
#define SSC1_TSRC_ADDR        0xF01002F4     /* "SSC Transmit Interrupt Service Request Control Register" */
#define SSC1_RSRC_ADDR        0xF01002F8     /* "SSC Receive Interrupt Service Request Control Register" */
#define SSC1_ESRC_ADDR        0xF01002FC     /* "SSC Error Interrupt Service Request Control Register" */


#endif /* _HAVE_TRICORE_SSC1_ADDRESSES_H_ (block "SSC0 LL1") */


