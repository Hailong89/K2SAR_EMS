/* block "ASC1 LL" of TriCore TC1766B (12 SFRs) */

#ifndef _HAVE_TRICORE_ASC1_ADDRESSES_H_
#define _HAVE_TRICORE_ASC1_ADDRESSES_H_

#define ASC1_PISEL_ADDR       0xF0000B04     /* "ASC Peripheral Input Select Register" */
#define ASC1_ID_ADDR          0xF0000B08     /* "ASC Module Identification Register" */
#define ASC1_CON_ADDR         0xF0000B10     /* "ASC Control Register" */
#define ASC1_BG_ADDR          0xF0000B14     /* "ASC Baudrate Timer Reload Register" */
#define ASC1_FDV_ADDR         0xF0000B18     /* "ASC Fractional Divider Register" */
#define ASC1_TBUF_ADDR        0xF0000B20     /* "ASC Transmit Buffer Register" */
#define ASC1_RBUF_ADDR        0xF0000B24     /* "ASC Receive Buffer Register" */
#define ASC1_WHBCON_ADDR      0xF0000B50     /* "ASC Hardware Bits Control Register" */
#define ASC1_TSRC_ADDR        0xF0000BF0     /* "ASC Transmit Interrupt Service Request Control Register" */
#define ASC1_RSRC_ADDR        0xF0000BF4     /* "ASC Receive Interrupt Service Request Control Register" */
#define ASC1_ESRC_ADDR        0xF0000BF8     /* "ASC Error Interrupt Service Request Control Register" */
#define ASC1_TBSRC_ADDR       0xF0000BFC     /* "ASC Transmit Buffer Service Request Control Register" */


#endif /* _HAVE_TRICORE_ASC1_ADDRESSES_H_ (block "ASC1 LL") */


