/* block "ASC0" of TriCore TC1167 (13 SFRs) */

#ifndef _HAVE_TRICORE_ASC0_ADDRESSES_H_
#define _HAVE_TRICORE_ASC0_ADDRESSES_H_

#define ASC0_CLC_ADDR         0xF0000A00     /* "ASC0 Clock Control Register" */
#define ASC0_PISEL_ADDR       0xF0000A04     /* "ASC0 Peripheral Input Select Register" */
#define ASC0_ID_ADDR          0xF0000A08     /* "Module Identification Register" */
#define ASC0_CON_ADDR         0xF0000A10     /* "Control Register" */
#define ASC0_BG_ADDR          0xF0000A14     /* "Baud Rate Timer/Reload Register" */
#define ASC0_FDV_ADDR         0xF0000A18     /* "Fractional Divider Register" */
#define ASC0_TBUF_ADDR        0xF0000A20     /* "Transmit Buffer Register" */
#define ASC0_RBUF_ADDR        0xF0000A24     /* "Receive Buffer Register" */
#define ASC0_WHBCON_ADDR      0xF0000A50     /* "Write Hardware Bits Control Register" */
#define ASC0_TSRC_ADDR        0xF0000AF0     /* "Transmit Interrupt Service Request Control Register" */
#define ASC0_RSRC_ADDR        0xF0000AF4     /* "Receive Interrupt Service Request Control Register" */
#define ASC0_ESRC_ADDR        0xF0000AF8     /* "Error Interrupt Service Request Control Register" */
#define ASC0_TBSRC_ADDR       0xF0000AFC     /* "Transmit Buffer Interrupt Service Request Control Register" */


#endif /* _HAVE_TRICORE_ASC0_ADDRESSES_H_ (block "ASC0") */


