#define MSC1_ID_ADDR          0xF0000908     /* "MSC1 Module Identification Register" */
#define MSC1_FDR_ADDR         0xF000090C     /* "MSC1 Fractional Divider Register" */
#define MSC1_USR_ADDR         0xF0000910     /* "MSC1 Upstream Status Register" */
#define MSC1_DSC_ADDR         0xF0000914     /* "MSC1 Downstream Control Register" */
#define MSC1_DSS_ADDR         0xF0000918     /* "MSC1 Downstream Status Register" */
#define MSC1_DD_ADDR          0xF000091C     /* "MSC1 Downstream Data Register" */
#define MSC1_DC_ADDR          0xF0000920     /* "MSC1 Downstream Command Register" */
#define MSC1_DSDSL_ADDR       0xF0000924     /* "MSC1 Downstream Select Data Source Low Register" */
#define MSC1_DSDSH_ADDR       0xF0000928     /* "MSC1 Downstream Select Data Source High Register" */
#define MSC1_ESR_ADDR         0xF000092C     /* "MSC1 Emergency Stop Register" */
#define MSC1_UD0_ADDR         0xF0000930     /* "MSC1 Upstream Data Register 0" */
#define MSC1_UD1_ADDR         0xF0000934     /* "MSC1 Upstream Data Register 1" */
#define MSC1_UD2_ADDR         0xF0000938     /* "MSC1 Upstream Data Register 2" */
#define MSC1_UD3_ADDR         0xF000093C     /* "MSC1 Upstream Data Register 3" */
#define MSC1_ICR_ADDR         0xF0000940     /* "MSC1 Interrupt Control Register" */
#define MSC1_ISR_ADDR         0xF0000944     /* "MSC1 Interrupt Status Register" */
#define MSC1_ISC_ADDR         0xF0000948     /* "MSC1 Interrupt Set Clear Register" */
#define MSC1_OCR_ADDR         0xF000094C     /* "MSC1 Output Control Register" */
#define MSC1_SRC1_ADDR        0xF00009F8     /* "MSC1 Service Request Control Register 1" */
#define MSC1_SRC0_ADDR        0xF00009FC     /* "MSC1 Service Request Control Register 0" */


#endif /* _HAVE_TRICORE_MSC1_ADDRESSES_H_ (block "MSC1") */


