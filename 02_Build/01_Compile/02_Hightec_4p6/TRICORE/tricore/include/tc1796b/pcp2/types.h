      unsigned int EIE:1;               /*  = [5..5] = 0x00000020 */
      unsigned int CS:2;                /*  = [6..7] = 0x000000c0 */
      unsigned int PPE:1;               /*  = [8..8] = 0x00000100 */
      unsigned int PPS:7;               /*  = [9..15] = 0x0000fe00 */
      unsigned int CWE:1;               /*  = [16..16] = 0x00010000 */
