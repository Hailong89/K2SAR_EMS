/* block "Port4" of TriCore TC1796B (9 SFRs) */

#ifndef _HAVE_TRICORE_P4_ADDRESSES_H_
#define _HAVE_TRICORE_P4_ADDRESSES_H_

#define P4_OUT_ADDR           0xF0001000     /* "Port 4 Output Register" */
#define P4_OMR_ADDR           0xF0001004     /* "Port 4 Output Modification Register" */
#define P4_IOCR0_ADDR         0xF0001010     /* "Port 4 Input/Output Control Register 0" */
#define P4_IOCR4_ADDR         0xF0001014     /* "Port 4 Input/Output Control Register 4" */
#define P4_IOCR8_ADDR         0xF0001018     /* "Port 4 Input/Output Control Register 8" */
#define P4_IOCR12_ADDR        0xF000101C     /* "Port 4 Input/Output Control Register 12" */
#define P4_IN_ADDR            0xF0001024     /* "Port 4 Input Register" */
#define P4_PDR_ADDR           0xF0001040     /* "Port 4 Pad Driver Mode Register" */
#define P4_ESR_ADDR           0xF0001050     /* "Port 4 Emergency Stop Register" */


#endif /* _HAVE_TRICORE_P4_ADDRESSES_H_ (block "Port4") */


