#ifndef _HAVE_TRICORE_MSC0_ADDRESSES_H_
#define _HAVE_TRICORE_MSC0_ADDRESSES_H_

#define MSC0_CLC_ADDR         0xF0000800     /* "MSC0 Clock Control Register" */
#define MSC0_ID_ADDR          0xF0000808     /* "MSC0 Module Identification Register" */
#define MSC0_FDR_ADDR         0xF000080C     /* "MSC0 Fractional Divider Register" */
#define MSC0_USR_ADDR         0xF0000810     /* "MSC0 Upstream Status Register" */
#define MSC0_DSC_ADDR         0xF0000814     /* "MSC0 Downstream Control Register" */
#define MSC0_DSS_ADDR         0xF0000818     /* "MSC0 Downstream Status Register" */
#define MSC0_DD_ADDR          0xF000081C     /* "MSC0 Downstream Data Register" */
#define MSC0_DC_ADDR          0xF0000820     /* "MSC0 Downstream Command Register" */
#define MSC0_DSDSL_ADDR       0xF0000824     /* "MSC0 Downstream Select Data Source Low Register" */
#define MSC0_DSDSH_ADDR       0xF0000828     /* "MSC0 Downstream Select Data Source High Register" */
#define MSC0_ESR_ADDR         0xF000082C     /* "MSC0 Emergency Stop Register" */
#define MSC0_UD0_ADDR         0xF0000830     /* "MSC0 Upstream Data Register 0" */
#define MSC0_UD1_ADDR         0xF0000834     /* "MSC0 Upstream Data Register 1" */
#define MSC0_UD2_ADDR         0xF0000838     /* "MSC0 Upstream Data Register 2" */
#define MSC0_UD3_ADDR         0xF000083C     /* "MSC0 Upstream Data Register 3" */
#define MSC0_ICR_ADDR         0xF0000840     /* "MSC0 Interrupt Control Register" */
#define MSC0_ISR_ADDR         0xF0000844     /* "MSC0 Interrupt Status Register" */
#define MSC0_ISC_ADDR         0xF0000848     /* "MSC0 Interrupt Set Clear Register" */
#define MSC0_OCR_ADDR         0xF000084C     /* "MSC0 Output Control Register" */
#define MSC0_SRC1_ADDR        0xF00008F8     /* "MSC0 Service Request Control Register 1" */
#define MSC0_SRC0_ADDR        0xF00008FC     /* "MSC0 Service Request Control Register 0" */


#endif /* _HAVE_TRICORE_MSC0_ADDRESSES_H_ (block "MSC0") */


