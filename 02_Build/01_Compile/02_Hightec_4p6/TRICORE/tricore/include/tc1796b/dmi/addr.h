#define DMI_ATR_ADDR          0xF87FFC20     /* "DMI Asynchronous Trap Flag Register" */
#define DMI_CON1_ADDR         0xF87FFC28     /* "DMI Control Register 1" */


#endif /* _HAVE_TRICORE_DMI_ADDRESSES_H_ (block "DMI") */


