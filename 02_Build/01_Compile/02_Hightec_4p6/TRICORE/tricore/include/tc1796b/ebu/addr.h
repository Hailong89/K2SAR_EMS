/* block "EBU" of TriCore TC1796B (21 SFRs) */

#ifndef _HAVE_TRICORE_EBU_ADDRESSES_H_
#define _HAVE_TRICORE_EBU_ADDRESSES_H_

#define EBU_CLC_ADDR          0xF8000000     /* "EBU Clock Control Register" */
#define EBU_ID_ADDR           0xF8000008     /* "EBU Module Identification Register" */
#define EBU_CON_ADDR          0xF8000010     /* "EBU Configuration Register" */
#define EBU_BFCON_ADDR        0xF8000020     /* "EBU Burst Flash Control Register" */
#define EBU_ADDRSEL0_ADDR     0xF8000080     /* "EBU Address Select Register 0" */
#define EBU_ADDRSEL1_ADDR     0xF8000088     /* "EBU Address Select Register 1" */
#define EBU_ADDRSEL2_ADDR     0xF8000090     /* "EBU Address Select Register 2" */
#define EBU_ADDRSEL3_ADDR     0xF8000098     /* "EBU Address Select Register 3" */
#define EBU_BUSCON0_ADDR      0xF80000C0     /* "EBU Bus Configuration Register 0" */
#define EBU_BUSCON1_ADDR      0xF80000C8     /* "EBU Bus Configuration Register 1" */
#define EBU_BUSCON2_ADDR      0xF80000D0     /* "EBU Bus Configuration Register 2" */
#define EBU_BUSCON3_ADDR      0xF80000D8     /* "EBU Bus Configuration Register 3" */
#define EBU_BUSAP0_ADDR       0xF8000100     /* "EBU Bus Access Parameter Register 0" */
#define EBU_BUSAP1_ADDR       0xF8000108     /* "EBU Bus Access Parameter Register 1" */
#define EBU_BUSAP2_ADDR       0xF8000110     /* "EBU Bus Access Parameter Register 2" */
#define EBU_BUSAP3_ADDR       0xF8000118     /* "EBU Bus Access Parameter Register 3" */
#define EBU_EMUAS_ADDR        0xF8000160     /* "EBU Emulator Address Select Register" */
#define EBU_EMUBC_ADDR        0xF8000168     /* "EBU Emulator Bus Configuration Register" */
#define EBU_EMUBAP_ADDR       0xF8000170     /* "EBU Emulator Bus Access Parameter Register" */
#define EBU_EMUOVL_ADDR       0xF8000178     /* "EBU Emulator Overlay Register" */
#define EBU_USERCON_ADDR      0xF8000190     /* "EBU Test/Control Configuration Register" */


#endif /* _HAVE_TRICORE_EBU_ADDRESSES_H_ (block "EBU") */


