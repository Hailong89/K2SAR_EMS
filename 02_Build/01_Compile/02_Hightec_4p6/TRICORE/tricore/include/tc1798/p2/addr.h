/* block "P2" of TriCore TC1798 (10 SFRs) */

#ifndef _HAVE_TRICORE_P2_ADDRESSES_H_
#define _HAVE_TRICORE_P2_ADDRESSES_H_

#define P2_OUT_ADDR           0xF0000E00     /* "Port 2 Output Register" */
#define P2_OMR_ADDR           0xF0000E04     /* "Port 2 Output Modification Register" */
#define P2_IOCR0_ADDR         0xF0000E10     /* "Port 2 Input/Output Control Register 0" */
#define P2_IOCR4_ADDR         0xF0000E14     /* "Port 2 Input/Output Control Register 4" */
#define P2_IOCR8_ADDR         0xF0000E18     /* "Port 2 Input/Output Control Register 8" */
#define P2_IOCR12_ADDR        0xF0000E1C     /* "Port 2 Input/Output Control Register 12" */
#define P2_IN_ADDR            0xF0000E24     /* "Port 2 Input Register" */
#define P2_PDR0_ADDR          0xF0000E40     /* "Port 2 Pad Driver Mode 0 Register" */
#define P2_PDR1_ADDR          0xF0000E44     /* "Port 2 Pad Driver Mode 1 Register" */
#define P2_ESR_ADDR           0xF0000E50     /* "Port 2 Emergency Stop Register" */


#endif /* _HAVE_TRICORE_P2_ADDRESSES_H_ (block "P2") */


