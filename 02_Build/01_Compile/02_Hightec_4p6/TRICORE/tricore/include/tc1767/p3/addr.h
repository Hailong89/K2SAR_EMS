#define P3_OMR_ADDR           0xF0000F04     /* "Port 3 Output Modification Register" */
#define P3_IOCR0_ADDR         0xF0000F10     /* "Port 3 Input/Output Control Register 0" */
#define P3_IOCR4_ADDR         0xF0000F14     /* "Port 3 Input/Output Control Register 4" */
#define P3_IOCR8_ADDR         0xF0000F18     /* "Port 3 Input/Output Control Register 8" */
#define P3_IOCR12_ADDR        0xF0000F1C     /* "Port 3 Input/Output Control Register 12" */
#define P3_IN_ADDR            0xF0000F24     /* "Port 3 Input Register" */
#define P3_PDR_ADDR           0xF0000F40     /* "Port 3 Pad Driver Mode Register" */
#define P3_ESR_ADDR           0xF0000F50     /* "Port 3 Emergency Stop Register" */


#endif /* _HAVE_TRICORE_P3_ADDRESSES_H_ (block "P3") */


