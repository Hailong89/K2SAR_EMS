/* block "SSC2" of TriCore TC1782 (15 SFRs) */

#ifndef _HAVE_TRICORE_SSC2_ADDRESSES_H_
#define _HAVE_TRICORE_SSC2_ADDRESSES_H_

#define SSC2_CLC_ADDR         0xF0100300     /* "SSC2 Clock Control Register" */
#define SSC2_PISEL_ADDR       0xF0100304     /* "Port Input Select Register" */
#define SSC2_ID_ADDR          0xF0100308     /* "Module Identification Register" */
#define SSC2_FDR_ADDR         0xF010030C     /* "SSC2 Fractional Divider Register" */
#define SSC2_CON_ADDR         0xF0100310     /* "Control Register" */
#define SSC2_BR_ADDR          0xF0100314     /* "Baud Rate Timer Reload Register" */
#define SSC2_SSOC_ADDR        0xF0100318     /* "Slave Select Output Control Register" */
#define SSC2_SSOTC_ADDR       0xF010031C     /* "Slave Select Output Timing Control Register" */
#define SSC2_TB_ADDR          0xF0100320     /* "Transmit Buffer Register" */
#define SSC2_RB_ADDR          0xF0100324     /* "Receive Buffer Register" */
#define SSC2_STAT_ADDR        0xF0100328     /* "Status Register" */
#define SSC2_EFM_ADDR         0xF010032C     /* "Error Flag Modification Register" */
#define SSC2_TSRC_ADDR        0xF01003F4     /* "Transmit Interrupt Service Request Control Register" */
#define SSC2_RSRC_ADDR        0xF01003F8     /* "Receive Interrupt Service Request Control Register" */
#define SSC2_ESRC_ADDR        0xF01003FC     /* "Error Interrupt Service Request Control Register" */


#endif /* _HAVE_TRICORE_SSC2_ADDRESSES_H_ (block "SSC2") */


